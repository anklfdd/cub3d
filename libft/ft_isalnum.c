#include "libft.h"

int	ft_isalnum(int sym)
{
	if ((sym >= '0' && sym <= '9')
		|| (sym >= 'A' && sym <= 'Z')
		|| (sym >= 'a' && sym <= 'z'))
		return (1);
	return (0);
}
